#import "UserManager.h"

@implementation UserManager

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark - NSObject

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (instancetype)init
{
    self = [super init];

    if (self) {
        self.banks = [[NSUserDefaults standardUserDefaults] objectForKey:kUserDefaultsBanks];
    }

    return self;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark - Public methods

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)save
{
    // Save account details
    [[NSUserDefaults standardUserDefaults] setObject:self.banks forKey:kUserDefaultsBanks];
}

@end
